/// Enum to define screen sizes
///
/// Screen siezes are based on [Material 3]
enum WindowSize {
  ///Compact screen size.
  ///
  ///Usually a portrait phone.
  compact,

  ///Medium screen size.
  ///
  ///Usually a landscape phone. or portrait tablet.
  medium,

  ///Expanded screen size.
  ///
  ///Usually a landscape tablet or a small desktop.
  expanded,

  ///Large screen size.
  ///
  ///Usually a large desktot or big landscape tablet.
  large,

  ///Extra large screen size.
  ///
  ///Usually a big desktop.
  extraLarge,
}
